package com.eniencheres.bll;

public class FormErrors {

    private String erreurNom;
    private String erreurDescription;
    private String erreurCat;
    private String erreurPrix;
    private String erreurDateDebut;
    private String erreurDateFin;

    public FormErrors() {
    }

    public String getErreurNom() {
        return erreurNom;
    }

    public void setErreurNom(String erreurNom) {
        this.erreurNom = erreurNom;
    }

    public String getErreurDescription() {
        return erreurDescription;
    }

    public void setErreurDescription(String erreurDescription) {
        this.erreurDescription = erreurDescription;
    }

    public String getErreurCat() {
        return erreurCat;
    }

    public void setErreurCat(String erreurCat) {
        this.erreurCat = erreurCat;
    }

    public String getErreurPrix() {
        return erreurPrix;
    }

    public void setErreurPrix(String erreurPrix) {
        this.erreurPrix = erreurPrix;
    }

    public String getErreurDateDebut() {
        return erreurDateDebut;
    }

    public void setErreurDateDebut(String erreurDateDebut) {
        this.erreurDateDebut = erreurDateDebut;
    }

    public String getErreurDateFin() {
        return erreurDateFin;
    }

    public void setErreurDateFin(String erreurDateFin) {
        this.erreurDateFin = erreurDateFin;
    }
}
